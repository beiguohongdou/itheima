package com.itheima.arithmeticoperator;

public class 算数运算符Demo2 {
    public static void main(String[] args) {
        System.out.println(10 / 2);
        System.out.println(10.0 / 3);
        System.out.println(10 % 2);
        System.out.println(10 % 3);
        //斗地主发牌
        //三个玩家，把每个牌都定义一个序号
        //拿着序号%3，如果结果为1，就发给第一个玩家
        //如果结果为2，那么就发给第二个玩家
        //如果结果为0，就发给第三个玩家
    }
}
